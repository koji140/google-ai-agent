const fs = require('fs');
const path = require('path');

const SOURCE_DIR = path.join(__dirname, 'apps-script', 'src', 'html');
const TARGET_PARENT_FOLDER_NAME = '03_議事録_打合せ';
const TARGET_FOLDER_NAME = '20260427_本社打合せ';
const TARGET_SUBFOLDER = 'previews/hp';
const OUTPUT_FILE = path.join(__dirname, 'apps-script', 'src', 'migration2.js');

const files = fs.readdirSync(SOURCE_DIR);
const fileDataArray = [];

for (const file of files) {
  if (!file.endsWith('.html')) continue;
  const content = fs.readFileSync(path.join(SOURCE_DIR, file), 'utf8');
  fileDataArray.push({
    name: file,
    mimeType: 'text/html',
    content: content
  });
}

const scriptContent = `
// AUTOGENERATED MIGRATION SCRIPT 2 (Components)
function runMigrationPhase2() {
  const pocFolderId = '1AtyDr5_VwcPlzxN8rJMDMbTqFtUlQAC0';
  const pocRoot = DriveApp.getFolderById(pocFolderId);
  
  // Get 03_議事録_打合せ -> 20260427_本社打合せ -> previews -> hp
  let current = pocRoot;
  const paths = ['${TARGET_PARENT_FOLDER_NAME}', '${TARGET_FOLDER_NAME}', 'previews', 'hp'];
  
  for (const p of paths) {
    const iter = current.getFoldersByName(p);
    if (iter.hasNext()) {
      current = iter.next();
    } else {
      current = current.createFolder(p);
    }
  }

  const hpFolder = current;
  const files = ${JSON.stringify(fileDataArray)};
  
  for (const file of files) {
    // Delete existing if any
    const existing = hpFolder.getFilesByName(file.name);
    while (existing.hasNext()) {
      existing.next().setTrashed(true);
    }
    
    // Create new
    const blob = Utilities.newBlob(file.content, file.mimeType, file.name);
    hpFolder.createFile(blob);
  }
  
  return "Phase 2 Component Migration Completed. Uploaded " + files.length + " files.";
}
`;

fs.writeFileSync(OUTPUT_FILE, scriptContent, 'utf8');
console.log('Migration script 2 generated successfully at ' + OUTPUT_FILE);
