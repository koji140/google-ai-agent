const fs = require('fs');
const path = require('path');

const SOURCE_DIR = 'C:\\Users\\ishim\\github\\kinsetsu-process\\docs\\briefings\\20260427-hq-meeting';
const TARGET_PARENT_FOLDER_NAME = '03_議事録_打合せ';
const TARGET_FOLDER_NAME = '20260427_本社打合せ';
